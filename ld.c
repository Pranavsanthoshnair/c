// A C program to find The byte size of some data types
#include <stdio.h>
int main(){
  printf("\nSize of integer=%ld",sizeof(int));
  printf("\nSize of float=%ld",sizeof(float));
  printf("\nSize of double=%ld",sizeof(double));
  printf("\nSize of char=%ld",sizeof(char));
  printf("\nSize of long int=%ld",sizeof(long int));
  printf("\nSize of short int=%ld",sizeof(short int));
  return 0;
}
